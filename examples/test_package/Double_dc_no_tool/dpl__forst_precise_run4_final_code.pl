stack_op(sort, Stack, SortedStack) :-
 

    get_top_n(Stack, 3, [X,Y,Z], Rest),
    sort3(X, Y, Z, Sorted),
    append(Sorted, Rest, SortedStack).

sort3(X, Y, Z, [A,B,C]) :-
    max_list([X,Y,Z], C),
    min_list([X,Y,Z], A),
    (   member(B, [X,Y,Z]), B \= A, B \= C -> true
    ;   B = A
    ).
get_top_n(Stack, N, Elements, Rest) :-
 length(Elements, N),
 append(Elements, Rest, Stack).
