stack_op(sort, Stack, SortedStack) :-
 

    get_top_n(Stack, 3, [A,B,C], Rest),
    sort3(A, B, C, S1, S2, S3),
    SortedStack = [S1,S2,S3|Rest].

sort3(A, B, C, X, Y, Z) :-
    min(A, B, MinAB),
    max(A, B, MaxAB),
    min(MinAB, C, X),
    max(MinAB, C, Temp),
    min(Temp, MaxAB, Y),
    max(Temp, MaxAB, Z).
get_top_n(Stack, N, Elements, Rest) :-
 length(Elements, N),
 append(Elements, Rest, Stack).
