stack_op(sort, Stack, SortedStack) :-
 
 
    get_top_n(Stack, 3, [X,Y,Z], Rest),
    sort3(X, Y, Z, S1, S2, S3),
    SortedStack = [S1,S2,S3|Rest].

sort3(X, Y, Z, S1, S2, S3) :-
    min_list([X,Y,Z], S1),
    max_list([X,Y,Z], S3),
    (   member(S2, [X,Y,Z]), S2 \= S1, S2 \= S3 -> true
    ;   S2 = X
    ).
get_top_n(Stack, N, Elements, Rest) :-
 length(Elements, N),
 append(Elements, Rest, Stack).
